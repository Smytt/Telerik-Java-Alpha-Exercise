    public int maxAreaOfIsland(int[][] grid) {
        int max = 0;
        int[][] directions = {{0, 1}, {0, -1}, {1, 0}, {-1, 0}};
        int rows = grid.length;
        int cols = grid[0].length;

        for (int r = 0; r < rows; r++) {
            for (int c = 0; c < cols; c++) {
                if (grid[r][c] == 1) {
                    max = Math.max(max, bfs(grid, r, c, directions));
                }
            }
        }

        return max;
    }

    private int bfs(int[][] grid, int r, int c, int[][] directions) {
        int land = 1;
        grid[r][c] = 0;

        for (int[] direction : directions) {
            if (isValid(grid, r, c, direction)) {
                land += bfs(grid, r + direction[0], c + direction[1], directions);
            }
        }

        return land;

    }

    private boolean isValid(int[][] grid, int thisR, int thisC, int[] direction) {
        int r = thisR + direction[0];
        int c = thisC + direction[1];
        return r >= 0 && r < grid.length && c >= 0 && c < grid[r].length && grid[r][c] == 1;
    }

    private Integer code(int r, int c, int codeFactor) {
        return r * codeFactor + c;
    }
